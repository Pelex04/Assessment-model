    -- Calculate the final grade for this specific student and subject
    DECLARE v_final_score DECIMAL(5,2);
    DECLARE v_passed BOOLEAN;
    
    -- Calculate the weighted sum of all assessment scores for this student and subject
    SELECT SUM((sg.score * aw.weight) / 100) INTO v_final_score
    FROM student_grades sg
    INNER JOIN assessment_weights aw 
        ON sg.subject_id = aw.subject_id 
        AND sg.assessment_type_id = aw.assessment_type_id
    WHERE sg.student_id = NEW.student_id
        AND sg.subject_id = NEW.subject_id
        AND sg.semester = NEW.semester
        AND sg.academic_year = NEW.academic_year;
    
    -- Determine if the student passed
    SET v_passed = IF(v_final_score >= 50, TRUE, FALSE);
    
    -- Insert or update the final_grades table
    INSERT INTO final_grades 
        (student_id, subject_id, semester, academic_year, final_score, passed)
    VALUES 
        (NEW.student_id, NEW.subject_id, NEW.semester, NEW.academic_year, v_final_score, v_passed)
    ON DUPLICATE KEY UPDATE
        final_score = v_final_score,
